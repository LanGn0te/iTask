    public void adicionarTarefa(String conteudo, String materia, String dataEntrega, String modoEntrega, String comentarios) {
        tarefa.nova(conteudo, materia, dataEntrega, modoEntrega, comentarios);
        listTarefas.add(conteudo + bar + materia + bar + dataEntrega + bar + modoEntrega);
    }
    
    private void recarregarListTarefas () {
            tarefa.recarregar();
        for (int x = 0; x == tarefa.getNumTarefas(); x++) {
            listTarefas.add(tarefa.getConteudo(x) + tarefa.getMateria(x) + tarefa.getDataEntrega(x) + tarefa.getModoEntrega(x) + tarefa.getComentario(x));
        }
    }

